// 두 수의 합
class Solution {
    public int solution (int num1, int num2) {
        int result;       
        result = num1 + num2;
        
        return result;
    } 
}

// 두 수의 차
class Solution {
    public int solution(int num1, int num2) {
        int answer = num1 - num2;
        return answer;
    }
}

// 두 수의 곱
class Solution {
    public int solution(int num1, int num2) {
        int answer = num1 * num2;
        return answer;
    }
}

// 몫 구하기
class Solution {
    public int solution(int num1, int num2) {
        int answer = num1 / num2;
        return answer;
    }
}
